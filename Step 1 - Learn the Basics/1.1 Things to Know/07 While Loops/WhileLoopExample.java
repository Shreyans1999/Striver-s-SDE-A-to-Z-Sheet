
// Java Example of While Loop

public class WhileLoopExample {
    public static void main(String[] args) {
        int counter = 0;
        System.out.println("Counting up to 10:");
        
        while (counter <= 10) {
            System.out.println("Counter: " + counter);
            counter++;
        }
        
        System.out.println("Finished!");
    }
}
