
# Python Example: Lambda Functions

# Lambda for addition
add = lambda x, y: x + y

# Lambda for squaring numbers
square = lambda x: x ** 2

# Lambda for checking even or odd
is_even = lambda x: 'Even' if x % 2 == 0 else 'Odd'

# Using the lambdas
print(f"Addition (5 + 7): {add(5, 7)}")
print(f"Square of 4: {square(4)}")
print(f"Number 9 is: {is_even(9)}")
