public class IfElseIfElse {
    public static void main(String[] args) {
        int num = 0;

        // if-else if-else statement
        if (num > 0) {
            System.out.println("Number is positive");
        } else if (num < 0) {
            System.out.println("Number is negative");
        } else {
            System.out.println("Number is zero");
        }
    }
}
