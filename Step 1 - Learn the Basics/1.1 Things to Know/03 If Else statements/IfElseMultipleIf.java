public class IfElseMultipleIf {
    public static void main(String[] args) {
        int num = 5;

        // Multiple if statements (independent checks)
        if (num > 0) {
            System.out.println("Number is positive");
        }
        if (num % 2 == 0) {
            System.out.println("Number is even");
        }
    }
}
